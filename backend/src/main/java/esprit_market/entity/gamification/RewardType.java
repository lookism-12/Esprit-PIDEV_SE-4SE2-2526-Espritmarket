package esprit_market.entity.gamification;

/**
 * Types of rewards available in the Wheel of Fortune
 */
public enum RewardType {
    DISCOUNT,       // Percentage discount on next order
    FREE_SHIPPING,  // Free shipping on next order
    POINTS,         // Loyalty points added to account
    COUPON,         // Special coupon code
    NO_LUCK         // Better luck next time
}
